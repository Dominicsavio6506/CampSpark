@login_required
@user_passes_test(is_admin)
def student_pdf(request):
    response = HttpResponse(content_type='application/pdf')
    response['Content-Disposition'] = 'attachment; filename="students_report.pdf"'

    doc = SimpleDocTemplate(response, pagesize=A4)
    elements = []
    styles = getSampleStyleSheet()
    normal_style = styles["Normal"]

    # ===== TITLE =====
    elements.append(Paragraph("CampSpark Student Report", styles['Title']))
    elements.append(Spacer(1, 20))

    students = Student.objects.all()

    # ===== TABLE HEADER =====
    data = [[
        "Roll No", "Name", "Department",
        "Year", "Phone", "Email"
    ]]

    for s in students:
        # WRAP TEXT SAFE
        name_para = Paragraph(s.name, normal_style)
        dept_para = Paragraph(s.department, normal_style)

        email = s.user.email if s.user else "N/A"

        row = [
            s.roll_number,
            name_para,
            dept_para,
            s.year,
            s.phone if s.phone else "N/A",
            email
        ]
        data.append(row)

    # ===== TABLE WIDTH CONTROL =====
    table = Table(
        data,
        repeatRows=1,
        colWidths=[70, 110, 120, 40, 90, 120]
    )

    # ===== TABLE STYLE =====
    table.setStyle(TableStyle([
        ('BACKGROUND', (0, 0), (-1, 0), colors.darkblue),
        ('TEXTCOLOR', (0, 0), (-1, 0), colors.white),

        ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
        ('FONTSIZE', (0, 0), (-1, -1), 9),

        ('ALIGN', (0, 0), (-1, -1), 'CENTER'),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),

        ('GRID', (0, 0), (-1, -1), 1, colors.black),
        ('BACKGROUND', (0, 1), (-1, -1), colors.whitesmoke),
    ]))

    elements.append(table)
    elements.append(Spacer(1, 20))

    # ===== FOOTER =====
    elements.append(Paragraph("Generated by CampSpark ERP", normal_style))
    elements.append(Paragraph("Authorized Signature", normal_style))

