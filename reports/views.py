def fee_certificate_pdf(request, student_id):
    student = get_object_or_404(Student, id=student_id)
    fees = Fee.objects.filter(student=student)
    total_paid = sum(f.paid_amount for f in fees)

    response = HttpResponse(content_type='application/pdf')
    response['Content-Disposition'] = 'attachment; filename="fee_certificate.pdf"'

    p = canvas.Canvas(response, pagesize=A4)
    width, height = A4

    # ===== OUTER BORDER (HALF PAGE) =====
    box_x = 70
    box_y = height - 420   # TOP HALF
    box_w = width - 140
    box_h = 360

    p.rect(box_x, box_y, box_w, box_h)

    # ===== COLLEGE NAME =====
    p.setFont("Helvetica-Bold", 18)
    p.drawCentredString(width / 2, box_y + box_h - 40,
                        "GOVERNMENT ARTS AND SCIENCE COLLEGE")

    p.setFont("Helvetica", 11)
    p.drawCentredString(width / 2, box_y + box_h - 60,
                        "Affiliated to State University")

    # ===== TITLE =====
    p.setFont("Helvetica-Bold", 14)
    p.drawCentredString(width / 2, box_y + box_h - 100,
                        "FEE PAID CERTIFICATE")

    # ===== BODY TEXT =====
    p.setFont("Helvetica", 11)
    text = f"""
This is to certify that {student.name},
Roll No {student.roll_number},
Department {student.department},
has successfully paid the total fee amount of ₹{total_paid}.
"""

    y_text = box_y + box_h - 150
    for line in text.split("\n"):
        p.drawString(box_x + 40, y_text, line)
        y_text -= 20

    # ===== SIGNATURE LINES =====
    p.line(box_x + 40, box_y + 60, box_x + 160, box_y + 60)
    p.drawString(box_x + 50, box_y + 40, "HOD Signature")

    p.line(box_x + box_w - 160, box_y + 60, box_x + box_w - 40, box_y + 60)
    p.drawString(box_x + box_w - 150, box_y + 40, "Principal Signature")

    # ===== FOOTER =====
    p.setFont("Helvetica-Oblique", 9)
    p.drawCentredString(width / 2, box_y + 15, "Generated by CampSpark ERP")

    p.showPage()
    p.save()
    return response