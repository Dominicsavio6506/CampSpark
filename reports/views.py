@login_required
@user_passes_test(is_admin)
def excel_report(request):
    wb = Workbook()

    # ================= SUMMARY SHEET =================
    ws = wb.active
    ws.title = "Summary"

    bold = Font(bold=True)

    ws['A1'] = "CampSpark Analytics Report"
    ws['A1'].font = Font(bold=True, size=16)

    ws['A3'] = "Generated On:"
    ws['B3'] = str(timezone.now())

    ws['A5'] = "Total Students"
    ws['B5'] = Student.objects.count()

    ws['A6'] = "Total Marks Entries"
    ws['B6'] = Marks.objects.count()

    ws['A7'] = "Total Fees Records"
    ws['B7'] = Fee.objects.count()

    # ================= STUDENT SHEET =================
    ws2 = wb.create_sheet("Students")
    headers = ["Roll No", "Name", "Department", "Year", "Phone"]
    ws2.append(headers)

    for cell in ws2[1]:
        cell.font = bold

    for s in Student.objects.all():
        ws2.append([
            s.roll_number,
            s.name,
            s.department,
            s.year,
            s.phone or "N/A"
        ])

    # ================= MARKS SHEET =================
    ws3 = wb.create_sheet("Marks")
    headers = ["Roll No", "Name", "Subject", "Internal", "Semester", "Total"]
    ws3.append(headers)

    for cell in ws3[1]:
        cell.font = bold

    for m in Marks.objects.select_related('student'):
        ws3.append([
            m.student.roll_number,
            m.student.name,
            m.subject,
            m.internal_mark,
            m.semester_mark,
            m.total
        ])

    # ================= ATTENDANCE SHEET =================
    ws4 = wb.create_sheet("Attendance")
    headers = ["Roll No", "Name", "Date", "Status"]
    ws4.append(headers)

    for cell in ws4[1]:
        cell.font = bold

    for r in AttendanceRecord.objects.select_related('student', 'attendance'):
        status = "Present" if r.present else "Absent"
        ws4.append([
            r.student.roll_number,
            r.student.name,
            str(r.attendance.date),
            status
        ])

    # ================= FEES SHEET =================
    ws5 = wb.create_sheet("Fees")
    headers = ["Roll No", "Name", "Total", "Paid", "Due", "Status"]
    ws5.append(headers)

    for cell in ws5[1]:
        cell.font = bold

    for f in Fee.objects.select_related('student'):
        ws5.append([
            f.student.roll_number,
            f.student.name,
            f.total_amount,
            f.paid_amount,
            f.due_amount,
            f.status
        ])

    # ================= RESPONSE =================
    response = HttpResponse(
        content_type='application/vnd.openxmlformats-officedocument.spreadsheetml.sheet'
    )
    response['Content-Disposition'] = 'attachment; filename="CampSpark_Report.xlsx"'

    wb.save(response)
    return response

@login_required
@user_passes_test(is_student)
def semester_result_pdf(request, student_id):
    student = get_object_or_404(Student, id=student_id)

    response = HttpResponse(content_type='application/pdf')
    response['Content-Disposition'] = f'attachment; filename="{student.name}_result.pdf"'

    doc = SimpleDocTemplate(response, pagesize=A4)
    elements = []
    styles = getSampleStyleSheet()
    normal = styles["Normal"]

    elements.append(Paragraph("Semester Result Sheet", styles['Title']))
    elements.append(Spacer(1, 20))

    # STUDENT INFO
    info = f"""
    <b>Name:</b> {student.name}<br/>
    <b>Department:</b> {student.department}<br/>
    <b>Semester:</b> {student.semester}<br/>
    """
    elements.append(Paragraph(info, normal))
    elements.append(Spacer(1, 15))

    marks = Marks.objects.filter(student=student)

    data = [["Subject", "Internal", "Semester", "Total", "Grade", "Result"]]

    grand_total = 0

    for m in marks:
        grade = m.grade()
        result = "PASS" if m.total >= 50 else "FAIL"
        grand_total += m.total

        data.append([
            m.subject,
            m.internal_mark,
            m.semester_mark,
            m.total,
            grade,
            result
        ])

    table = Table(data, repeatRows=1)
    table.setStyle(TableStyle([
        ('GRID', (0,0), (-1,-1), 1, colors.black),
        ('BACKGROUND', (0,0), (-1,0), colors.darkblue),
        ('TEXTCOLOR', (0,0), (-1,0), colors.white),
    ]))

    elements.append(table)
    elements.append(Spacer(1, 20))

    elements.append(Paragraph(f"<b>Grand Total:</b> {grand_total}", normal))

    doc.build(elements)
    return response

